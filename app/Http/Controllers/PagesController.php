     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request)
    {
        $user=User::find($request->id);
        $user->id=$request->id;
        $user->name=$request->name;
        $user->email=$request->email;
        $user->save();
        return redirect('show');
    }

